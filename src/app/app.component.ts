import { Component, OnInit, OnDestroy, inject, NgZone, PLATFORM_ID } from '@angular/core';
import { CommonModule, isPlatformBrowser } from '@angular/common';
import {
  RouterOutlet,
  Router,
  NavigationStart,
  NavigationEnd,
} from '@angular/router';
import { HeaderComponent } from '../header/header.component';
import { FooterComponent } from '../footer/footer.component';
import { NavbarComponent } from '../navbar/navbar.component';
import { environment } from '../environments/environment';
import { ReactiveFormsModule } from '@angular/forms';
import { AuthService } from '../services/auth.service';
import { Auth, User } from '@angular/fire/auth';
import { filter, take, switchMap, tap, takeUntil } from 'rxjs/operators';
import { Subject } from 'rxjs';
import { AppCheckService } from '../services/app-check.service';

declare let gtag: Function;

@Component({
  selector: 'app-root',
  standalone: true,
  imports: [
    RouterOutlet,
    CommonModule,
    HeaderComponent,
    FooterComponent,
    NavbarComponent,
    ReactiveFormsModule,
  ],
  templateUrl: './app.component.html',
  styleUrl: './app.component.css',
})
export class AppComponent implements OnInit, OnDestroy {
  // ‚úÖ Angular 18 Best Practice: Use inject() for dependency injection
  private readonly appCheckService = inject(AppCheckService);
  private readonly auth = inject(Auth);
  private readonly authService = inject(AuthService);
  private readonly router = inject(Router);
  private readonly ngZone = inject(NgZone);
  private readonly platformId = inject(PLATFORM_ID);

  // ‚úÖ Angular 18 Best Practice: Proper cleanup with takeUntil
  private readonly destroy$ = new Subject<void>();

  title = 'Daily Loan Post';
  isRoleSelectionModalOpen = false;
  isAppInitialized = false;

  constructor() {
    // ‚úÖ Angular 18 Best Practice: Keep constructor minimal
  }

  async ngOnInit(): Promise<void> {
    try {
      // ‚úÖ Initialize App Check first for security
     // await this.initializeAppCheck();
      
      this.logEnvironmentInfo();
      this.setupNavigationMonitoring();
      this.setupAuthStateManagement();
      this.handleEmailVerification();
      this.logFirebaseAuthStatus();
      
      this.isAppInitialized = true;
    } catch (error) {
      console.error('Error during app initialization:', error);
      // Still allow app to continue if App Check fails
      this.isAppInitialized = true;
    }
  }

  ngOnDestroy(): void {
    // ‚úÖ Angular 18 Best Practice: Proper cleanup
    this.destroy$.next();
    this.destroy$.complete();
  }

  /**
   * ‚úÖ Angular 18 Best Practice: Initialize App Check with proper error handling
   
  private async initializeAppCheck(): Promise<void> {
    if (!isPlatformBrowser(this.platformId)) {
      console.log('‚ö™ Skipping App Check - not in browser environment');
      return;
    }

    try {
      console.log('üîê Initializing App Check...');
      await this.appCheckService.initializeAppCheck();
      console.log('‚úÖ App Check initialization completed');
    } catch (error) {
      console.error('‚ùå App Check initialization failed:', error);
      // Don't throw - allow app to continue
    }
  }

  /**
   * ‚úÖ Angular 18 Best Practice: Extract environment logging to separate method
   */
  private logEnvironmentInfo(): void {
    if (!environment.production) {
      console.log(
        'üåç Environment:',
        environment.production ? 'Production' : 'Development',
        'üîë Firebase API Key exists:',
        !!environment.firebase.apiKey,
        'üìè API Key length:',
        environment.firebase.apiKey ? environment.firebase.apiKey.length : 0,
        'üõ°Ô∏è App Check Key exists:',
        !!environment.appCheckSiteKey
      );
    }
  }

  /**
   * ‚úÖ Angular 18 Best Practice: Extract navigation monitoring with proper cleanup
   */
  private setupNavigationMonitoring(): void {
    this.router.events
      .pipe(
        filter(
          (event) =>
            event instanceof NavigationStart || event instanceof NavigationEnd
        ),
        takeUntil(this.destroy$)
      )
      .subscribe((event) => {
        if (event instanceof NavigationStart) {
          console.log('üß≠ Navigation starting to:', event.url);
          if (isPlatformBrowser(this.platformId)) {
            console.log(
              'üìç Redirect URL in storage:',
              localStorage.getItem('redirectUrl')
            );
          }
        }
        
        if (event instanceof NavigationEnd) {
          console.log('‚úÖ Navigation completed to:', event.url);
          
          if (isPlatformBrowser(this.platformId)) {
            // Clear the redirectUrl after successful navigation
            const storedRedirectUrl = localStorage.getItem('redirectUrl');
            if (event.url === storedRedirectUrl) {
              console.log('üóëÔ∏è Clearing redirect URL from storage');
              localStorage.removeItem('redirectUrl');
            }
          }
        }
      });
  }

  /**
   * ‚úÖ Angular 18 Best Practice: Extract auth state management with proper cleanup
   */
  private setupAuthStateManagement(): void {
    // Check auth state and handle redirects
    this.authService.authReady$
      .pipe(
        filter((ready) => ready),
        take(1),
        switchMap(() => this.authService.isLoggedIn$),
        takeUntil(this.destroy$)
      )
      .subscribe((isLoggedIn) => {
        console.log('üîê Auth initialization complete, user logged in:', isLoggedIn);

        if (!isPlatformBrowser(this.platformId)) {
          return; // Skip localStorage operations on server
        }

        // Clear any URL if on home page
        if (this.router.url === '/') {
          localStorage.removeItem('redirectUrl');
        }

        // If user is logged in but on login page, redirect to dashboard/home
        if (isLoggedIn && this.router.url.includes('/login')) {
          console.log('üîÑ User is authenticated, redirecting from login');
          const redirectUrl = localStorage.getItem('redirectUrl');

          // Clear the redirect URL before navigating
          localStorage.removeItem('redirectUrl');

          // Navigate to dashboard or home
          this.router.navigate([redirectUrl || '/dashboard']);
        }
        // If user is not logged in and needs auth, redirect to login
        else if (
          !isLoggedIn &&
          !this.isPublicRoute(this.router.url)
        ) {
          console.log('üö´ User not authenticated, redirecting to login');
          localStorage.setItem('redirectUrl', this.router.url);
          this.router.navigate(['/login']);
        }
      });

    // Monitor auth state changes
    this.authService.isLoggedIn$
      .pipe(takeUntil(this.destroy$))
      .subscribe((isLoggedIn) => {
        console.log('üîÑ Auth state changed:', isLoggedIn);
        
        if (isPlatformBrowser(this.platformId)) {
          console.log(
            'üíæ localStorage isLoggedIn:',
            localStorage.getItem('isLoggedIn')
          );
        }
      });
  }

  /**
   * ‚úÖ Angular 18 Best Practice: Helper method for route checking
   */
  private isPublicRoute(url: string): boolean {
    const publicRoutes = ['/login', '/', '/pricing', '/register', '/forgot-password'];
    return publicRoutes.some(route => url.includes(route));
  }

  /**
   * ‚úÖ Angular 18 Best Practice: Extract Firebase auth status logging
   */
  private logFirebaseAuthStatus(): void {
    if (!environment.production) {
      console.log('üî• Firebase Auth initialized:', !!this.auth);
      console.log('üõ°Ô∏è App Check initialized:', this.appCheckService.isInitialized());
    }
  }

  /**
   * ‚úÖ Angular 18 Best Practice: Keep modal methods concise
   */
  openRoleModalFromNavbar(): void {
    console.log('üìù NAVBAR: Opening role selection modal');
    this.isRoleSelectionModalOpen = true;
  }

  /**
   * ‚úÖ Angular 18 Best Practice: Close modal method
   */
  closeRoleModal(): void {
    this.isRoleSelectionModalOpen = false;
  }

  /**
   * ‚úÖ Angular 18 Best Practice: Email verification with proper cleanup and error handling
   */
  private handleEmailVerification(): void {
    console.log('üìß Checking for email verification link...');

    // Check if the current URL is a sign-in link
    this.authService
      .isEmailSignInLink()
      .pipe(
        take(1),
        tap((isSignInLink) => {
          console.log('üîó Is email sign-in link?', isSignInLink);

          if (isSignInLink) {
            this.handleEmailSignInLink();
          } else if (window.location.pathname === '/') {
            this.handleHomePageAuth();
          }
        }),
        takeUntil(this.destroy$)
      )
      .subscribe();
  }

  /**
   * ‚úÖ Angular 18 Best Practice: Extract email sign-in link handling
   */
  private handleEmailSignInLink(): void {
    console.log('‚úâÔ∏è Email sign-in link detected, handling authentication flow');

    if (!isPlatformBrowser(this.platformId)) {
      return;
    }

    // Get stored email
    const storedEmail = this.authService.getStoredEmail();
    console.log('üì® Stored email found?', !!storedEmail);

    if (storedEmail) {
      // Attempt to sign in with the email link
      this.authService.signInWithEmailLink(storedEmail)
        .pipe(takeUntil(this.destroy$))
        .subscribe({
          next: (user: User | null) => {
            if (user) {
              console.log('‚úÖ Sign-in with email link successful:', user.email);
              this.handleSuccessfulAuth();
            } else {
              console.log('‚ùå Sign-in with email link failed, redirecting to login');
              this.router.navigate(['/login']);
            }
          },
          error: (error: Error) => {
            console.error('‚ùå Error signing in with email link:', error);
            this.router.navigate(['/login']);
          },
        });
    } else {
      console.log('üìß No stored email found, redirecting to login for manual entry');
      this.router.navigate(['/login']);
    }
  }

  /**
   * ‚úÖ Angular 18 Best Practice: Extract successful auth handling
   */
  private handleSuccessfulAuth(): void {
    if (isPlatformBrowser(this.platformId)) {
      // Set login state explicitly
      localStorage.setItem('isLoggedIn', 'true');
    }

    // Navigate to dashboard
    this.ngZone.run(() => {
      console.log('üè† Navigating to dashboard after successful auth');
      
      if (
        this.router.url === '/login' ||
        this.router.url.includes('/login/verify')
      ) {
        const redirectUrl = isPlatformBrowser(this.platformId) 
          ? localStorage.getItem('redirectUrl') || '/dashboard'
          : '/dashboard';
          
        this.router.navigate([redirectUrl]);
        
        if (isPlatformBrowser(this.platformId)) {
          localStorage.removeItem('redirectUrl');
        }
      }
    });
  }

  /**
   * ‚úÖ Angular 18 Best Practice: Extract home page auth handling
   */
  private handleHomePageAuth(): void {
    // Check if user is authenticated and at login page, redirect to dashboard if needed
    this.authService.authReady$
      .pipe(
        filter((ready) => ready),
        take(1),
        switchMap(() => this.authService.isLoggedIn$),
        take(1),
        takeUntil(this.destroy$)
      )
      .subscribe((isLoggedIn) => {
        // Only redirect if we're on the login page
        if (
          isLoggedIn &&
          (this.router.url === '/login' ||
            this.router.url.includes('/login/verify'))
        ) {
          console.log('üîÑ User is authenticated at login page, redirecting to dashboard');
          this.ngZone.run(() => {
            this.router.navigate(['/dashboard']);
          });
        }
      });
  }
}